//Crie uma variável x inicializada com 0. Use um for com condição no meio para imprimir os números de 0 a 5.

package main

import "fmt"

func main() {
	x := 0 // condição 

	for x < 6 {// condição para continua a fazer, laço for continuará executando enquanto a condição  for verdadeira.
		fmt.Println(x)
		x++// acrescenta
	}
}
