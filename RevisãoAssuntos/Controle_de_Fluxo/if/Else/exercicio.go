// Crie uma variável idade. Se idade for maior ou igual a 18, imprima "Maior de idade", senão imprima "Menor de idade"
package main

import "fmt"

func main() {
	var idade int = 18

	if idade >= 18{
		fmt.Println("É maior de idade")
	}else {
		fmt.Println("É menor de idade")
	}
}


