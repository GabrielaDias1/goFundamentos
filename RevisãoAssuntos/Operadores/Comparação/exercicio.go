/*Crie duas variáveis inteiras x e y. Compare se x é maior que y, igual a y e diferente de y, imprimindo os resultados (true/false).*/
package main

import "fmt"

func main() {
	var x int = 5
	var y int = 3

	fmt.Println(x > y)
	fmt.Println(x == y)
	fmt.Println(x != y)
}

