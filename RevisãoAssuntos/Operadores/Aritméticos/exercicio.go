/*Crie duas variáveis inteiras, faça soma, subtração, multiplicação, divisão e módulo, e imprima o resultado de cada operação.*/
package main

import "fmt"

func main() {
	var numero1 int = 10
	var numero2 int = 10

	soma := numero1 + numero2
	subtracao := numero1 - numero2
	multiplicacao := numero1 * numero2
	divisao := numero1 / numero2
	mod := numero1 % numero2

	fmt.Println("Soma:", soma)
	fmt.Println("Subtração:", subtracao)
	fmt.Println("Multiplicação:", multiplicacao)
	fmt.Println("Divisão:", divisao)
	fmt.Println("Módulo da divisão:", mod)

}

/*Crie duas variáveis inteiras, faça soma, subtração, 
multiplicação, divisão e módulo, e imprima o resultado de cada operação.*/